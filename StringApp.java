public class StringApp {
    public static void main(String[] args) {
        
        System.out.println("Hello World"); // String
        System.out.println('H'); // Character '와 "를 구분해서 사용해야하는 듯 싶다.
        System.out.println(("H")); // 이것 역시 Character

        System.out.println("Hello \n World"); //  줄바꿈 = \n (new line)
        System.out.println("Hello \"World\""); // escape

    }
    
}
